import Ember from 'ember';
import layout from '../templates/components/x-modal';

export default Ember.Component.extend({
  layout: layout,

  actions: {

    ok: function() {
      this.$('.modal').modal('hide');
      this.sendAction('ok');
    }
  },

    show: function() {
      this.$('.modal').modal().on('hidden.bs.modal', function() {
      this.sendAction('close');
    }.bind(this));
  }.on('didInsertElement')
});
